def fry(min, source) :              # def: 사용자가 만든 함수 fry()
    print('\n튀김옷 입히기')             # 들여쓰기부터 시작
    print(min, '분 튀기기')
    print('소스 바르기 =', source)       # fry() 함수 끝

fry(4, '갈비 소스')                 # 본문코드 시작: 왕갈비 통닭
fry(6, '없음')                      # 후라닭
fry(5, '고천 소스')                 # 고천 통닭
fry(7, '불 소스')                   # 60배 불닭
